export type AppT = typeof APP_T.pt;

const LangContext = createContext<{ lang: Lang; setLang: (l: Lang) => void; AT: AppT }>({
  lang: 'pt', setLang: () => {}, AT: APP_T.pt,
});

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const s = localStorage.getItem('trokvibe_lang') as Lang | null;
    if (s === 'en' || s === 'es') return s;
    return 'pt';
  });

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem('trokvibe_lang', l);
  };

  return (
    <LangContext.Provider value={{ lang, setLang, AT: APP_T[lang] }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLang() {
  return useContext(LangContext);
}
